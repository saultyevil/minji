#include <math.h>

#include "minji.h"

/* ************************************************************************** */
/**
* @brief
*
* @details
*
* *************************************************************************** */

extern double
dot_product(const double *u, const double *v)
{
  return u[0] * v[0] + u[1] * v[1] + u[2] * v[2];
}

/* ************************************************************************** */
/**
* @brief
*
* @details
*
* *************************************************************************** */

extern double
vector_magnitude(const double *u)
{
  return sqrt(u[0] * u[0] + u[1] * u[1] + u[2] * u[2]);
}

/* ************************************************************************** */
/**
* @brief
*
* @details
*
* *************************************************************************** */

extern void
subtract_vectors(const double *u, const double *v, double *w)
{
  w[0] = v[0] - u[0];
  w[1] = v[1] - u[1];
  w[2] = v[2] - u[2];
}
